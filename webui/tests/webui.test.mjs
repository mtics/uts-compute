test("POST without application/json is rejected (CSRF guard)", async () => {
  const res = await fetch(base + "/api/actions/clone", {
    method: "POST",
    headers: { "content-type": "text/plain" },
    body: "sourceRunId=x"
  });
  const body = await res.json();
  assert.equal(res.status, 403);
  assert.equal(body.ok, false);
  assert.match(body.error, /same-origin|application\/json/i);
});

