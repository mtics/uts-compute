// Isolate the webui server's runtime writes into a fresh per-process temp dir (Bug P2). The in-process
// planner/audit code now defaults its `.uts-computing` root to a per-user state dir, so without this
// the seeded runs would either pollute the real user dir or fail the runtime-containment guard.
// realpathSync canonicalizes the /var -> /private/var symlink so server-resolved paths compare equal.
const testHome = fs.realpathSync(fs.mkdtempSync(path.join(os.tmpdir(), "uts-webui-test-")));
process.env.UTS_COMPUTING_HOME = testHome;
process.once("exit", () => {
  try {
    fs.rmSync(testHome, { recursive: true, force: true });
  } catch {
    /* best-effort: the OS reclaims the temp dir regardless */
  }
});

function tempRuntimeDir(prefix) {
  const dir = path.join(testHome, ".uts-computing", `${prefix}-${process.pid}-${Date.now()}-${Math.random().toString(16).slice(2)}`);
  fs.mkdirSync(dir, { recursive: true });
  return dir;
}
function readExample(name) {
  return JSON.parse(fs.readFileSync(path.join(repoRoot, "examples/jobs", name), "utf8"));
}

